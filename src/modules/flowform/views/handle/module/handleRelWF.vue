<template>
  <div class="handleRelWfVue designItem" v-show="isVisible">

      <ecoField :titleWidth="mItem.titleWidth?mItem.titleWidth:defaultTitleWidth" :bgColor="mItem.bgColor?mItem.bgColor:mForm?mForm.titleBgColor:null" 
            :titlePos="mItem.titlePos" :required="isRequired" v-bind:class="{'iptReadonly':(!isEditable || isReadonly)}" class="handleField"
            :textAlign="mItem.titleAlign"
            :verticalAlign="mItem.verticalAlign?mItem.verticalAlign:'top'"
        >
            <div slot="label"  v-bind:style="{textAlign:mItem.titleAlign}">
                    <div class="labelTitle" >
                        <i v-if="isRequired && mItem.titleAlign != 'left'" class="el-form-required-i labelTitleRequestI">*</i>
                        <span v-bind:style="{color:mItem.ftColor?mItem.ftColor:mForm?mForm.titleTextColor:null}">{{mItem.itemName}}</span>
                        <el-tooltip class="item" effect="dark" :content="errMsg" placement="left" :value='errTip' >
                                <span></span>
                        </el-tooltip>
                    </div>
            </div>

            <div slot="content"> 
                    <el-input 
                            v-model="value"  
                            readonly
                            v-bind:class="{'iptReadonly':(!isEditable || isReadonly),'iptPage':hasClick,'pointerCalss':hasClick}"
                            :placeholder="(mItem.inst && mItem.inst !='')?mItem.inst:''"
                            ref="iptRef"
                            @change="onChangeEvent"
                            @blur="onblurEvent"
                            @click.native="onClickEvent"
                            :id="'handleItem-'+mItem.viewId+'-'+mItem.itemId"
                            v-if="isEditable"
                    ></el-input>

                    <div v-else class="viewRelWfDiv" v-bind:class="{'iptReadonlyDiv':!isEditable}">
                        <span class="viewRelWfClass" v-for="(item,idx) in selItems" :key="idx" @click="goRefWf(item)">
                            {{item.value}}
                        </span>
                        &nbsp;
                    </div>

            </div> 
     </ecoField>
      
  </div>
</template>
<script>
import {getWFViewOperateId} from '../../../service/service.js'
import ecoField from '../../components/ecoField'
import {EcoUtil} from '@/components/util/main.js'
import {EcoMessageBox} from '@/components/messageBox/main.js'
import {defaultTitleWidth,defaultFTColor}  from'../../../config/setting.js'

export default{
  name:'handleInput',
  components:{
      ecoField
  },
  props:{
        mItem:{
            type:Object
        },
        mValue:{
            type:Object
        },

        mForm:{
            type:Object
        }
  },
  data(){
        return {
            value:'',
            hiddenValue:'',

            isRequired:false,
            isVisible:true, //是否可见
            isReadonly:false, //是否只读
            isEditable:true, //是否需要上传后台
            hasClick:false,
            iptType:'',

            errTip:false,
            errMsg:'',

            selItems:[],
        }
  },
  created(){
       
  },
  mounted(){
       if(this.mItem && this.mItem.nullable == 0){
           this.isRequired = true;
       }
       if(this.mItem && this.mItem.visiable == 0){
           this.isVisible = false;
       }
       if(this.mItem && this.mItem.isReadonly == 1 ){
           this.isReadonly = true;
       }

       if(this.mItem && this.mItem.editable == 0){
           this.isEditable = false;
       }

       if((this.mValue.onclickEvents && this.mValue.onclickEvents.length > 0)){
            this.hasClick = true;
       }
       /*类型*/
       if(this.mValue && this.mValue.valType == 'NUMBER'){
           this.iptType = 'number';
       }
        
        if( this.mValue.hiddenValue  && null!=this.mValue.hiddenValue && this.mValue.hiddenValue!=''){
            let _hiddenValueArr = String(this.mValue.hiddenValue).split(',');
            let _valueArr = this.mValue.value.split('####');
            let _selItems = [];
            for(let i = 0;i<_hiddenValueArr.length;i++){
                 _selItems.push({value:_valueArr[i],hiddenValue:_hiddenValueArr[i]});
            }
            this.selItems = _selItems;
            this.value = _valueArr.join('    '); 
            this.hiddenValue = _hiddenValueArr.join(',')
        }

      
      
  },
  computed:{
    
  },
  methods: {

       /*触发点击焦点事件*/
       onClickEvent(){     
             if(this.isEditable && (!this.isReadonly)){   
                    (this.mValue.onclickEvents).forEach((eventKey)=>{
                        let _emit = {};
                        _emit.action = 'onEventKeyAction'
                        _emit.data = {};
                        _emit.data.itemId = this.mItem.itemId;
                        _emit.data.eventKey = eventKey;
                        this.$emit('emitEvent',_emit); 
                    })
            }
       },

       /*失去焦点事件*/ 
       onblurEvent(){
            /*数字类型的判断*/
            if(this.iptType == 'number'){
                if(this.value && this.value !=''){
                    if( !EcoUtil.isNumber(this.value)){
                        this.$refs.iptRef.blur();
                        EcoMessageBox.alert('请检查'+this.mItem.itemName+'格式是否正确');
                        return ;
                    }
                }
            }

            if(this.isEditable && (!this.isReadonly)){
                (this.mValue.onblurEvents).forEach((eventKey)=>{
                            let _emit = {};
                            _emit.action = 'onEventKeyAction'
                            _emit.data = {};
                            _emit.data.itemId = this.mItem.itemId;
                            _emit.data.eventKey = eventKey;
                            this.$emit('emitEvent',_emit); 
                })
            }
            
        },

        /*chang事件*/
        onChangeEvent(){ 
            (this.mValue.onchangeEvents).forEach((eventKey)=>{
                    let _emit = {};
                    _emit.action = 'onEventKeyAction'
                    _emit.data = {};
                    _emit.data.itemId = this.mItem.itemId;
                    _emit.data.eventKey = eventKey;
                    this.$emit('emitEvent',_emit); 
            });

            this.onInteraction();
            this.emitMirrorEvent();
        },

        /*相关性*/
        onInteraction(){
            if(this.mValue.hasInteraction){
                    let _emit = {};
                    _emit.action = 'onInteractionAction'
                    _emit.data = {};
                    _emit.data.itemId = this.mItem.itemId;
                    this.$emit('emitEvent',_emit);
            }
        },

       

        /*Override 获取某个item的值*/
        getItemInputParamsValue(){  
             let _obj = {};
             _obj.value = this.value
             _obj.hiddenValue = this.hiddenValue;
             return _obj;
        },

        /*Override 设置item的值*/
        setItemOutputParamsValue(value,hiddenValue,fromItemId){ 
            this.value = value;
            this.hiddenValue = hiddenValue;
            if(this.mItem.itemId != fromItemId){ //如果触发元不是本组件，接着触发本组件的chang事件
                    this.onChangeEvent();
                    this.onblurEvent();
            }
        },

        /*设置相关性*/
        setItemInteraction(event){
            if(event.eventType == 'SET_VISIBALE'){
                 if(event.eventSource.val == 0){
                     this.isVisible = false;
                 }else{
                     this.isVisible = true;
                 }

                 this.emitItemVisibleAction(event.eventSource.val);
            }else if(event.eventType == 'SET_NULLABLE'){
                if(event.eventSource.val == 0){
                    this.isRequired = true;
                }else{
                    this.isRequired = false;
                }
            } else if(event.eventType == 'SET_READONLY'){
                if(event.eventSource.val == 1){
                    this.isReadonly = true;
                }else{
                    this.isReadonly = false;
                }
           }else if(event.eventType == 'SET_VAL'){
                 this.value = event.eventSource.val.value;
                 this.hiddenValue = event.eventSource.val.hiddenValue;
                 
                 this.onChangeEvent(); //触发改变事件
                 this.onblurEvent(); //触发失去焦点事件
               
                 
            }
        },


         emitItemVisibleAction(visiable){
             let _emit = {};
             _emit.action = 'onItemVisibleAction'
             _emit.data = {};
             _emit.data.itemId = this.mItem.itemId;
             _emit.data.visiable = visiable;
             this.$emit('emitEvent',_emit); 
        },

        /*接受事件的回写*/
       callEvent(obj,outputParams){ 
            if(obj.action == 'PAGE' || obj.action == 'APIPAGE'){ 
                if(outputParams.name !='out0'){
                     (obj.selItems).forEach((element,idx)=>{
                          this.value = element[outputParams.name];
                     })
                }else{
                      /*全部赋值*/
                      if(obj.selItems){
                            let _value = '';
                            let _hiddenValue = '';
                            (obj.selItems).forEach((element,idx)=>{
                                    if(_hiddenValue !=''){
                                        _hiddenValue +='|';
                                    }
                                    if(_value!=''){
                                        _value+=';';
                                    }
                                    _hiddenValue += element.hiddenValue;
                                    _value += element.value;
                            })
                            this.value = _value;
                            this.hiddenValue = _hiddenValue;
                    }
                }
            }

            this.onChangeEvent();
            this.onblurEvent();
         
       },


       callRelWFEvent(selItems){
             let _valueArr = [];
             let _hiddenValueArr = [];
             this.selItems = EcoUtil.objDeepCopy(selItems);
             (selItems).forEach((element,idx)=>{
                    _valueArr.push(element.value);
                    _hiddenValueArr.push(element.hiddenValue);
             })
             
             this.value = _valueArr.join('    '); 
             this.hiddenValue = _hiddenValueArr.join(',')
             this.onblurEvent();
             this.onChangeEvent();
        },

        /*提交的时候，获取*/
        getRefValue(){ 
             let _obj = null;
             if(this.isEditable){ //isEditable 提交
                 _obj  = {};
                 let _valueArr = [];
                 (this.selItems).forEach((element,idx)=>{
                      _valueArr.push({key:element.hiddenValue,value:element.value});
                 })
                 _obj.value = JSON.stringify(_valueArr);
             }
             return _obj;
       },

       /*检查 是否可以提交*/
       getRefCheck(){ 
            if(this.isEditable ){
                if(this.isRequired ){
                    if(!this.value || this.value == ''){
                        return { status:1,msg:this.mItem.itemName+' 必须填写',checkType:'TEXTFIELD',checkId:'handleItem-'+this.mItem.viewId+'-'+this.mItem.itemId,itemId:this.mItem.itemId} ;
                    }
                }
            }
            return {status:0}
        },

      doRefCheck(obj){
            try{
                   this.errTip = true;
                   this.errMsg = obj.msg;
                   let that = this;
                   setTimeout(function(){
                        that.errTip = false;
                   },2000);
                //    document.getElementById('handleItem'+this.mItem.itemId).scrollIntoView(true);
                   document.getElementById('handleItem-'+this.mItem.viewId+'-'+this.mItem.itemId).focus();
            }catch(e){
                console.log(e);
            }
        },

        goRefWf(item){
            let formView = 1;
            let _wfId = item.hiddenValue;
            let _ccId = null;
            getWFViewOperateId(_wfId,formView,_ccId).then((response)=>{
                    if(response.data.status <= 99){
                            let operateId = response.data.operate_id;
                            let goPage = "/flowform/index.html#/wfViewDetail/"+item.hiddenValue+"/"+operateId;
                            window.open(goPage,'','height=800,width=830,status=yes,scrollbars=yes,resizable=yes');
                    }else{
                            EcoMessageBox.alert(response.data.msg);
                    }
            })
        },

        /*镜像映射*/
        emitMirrorEvent(){ 
            if(this.mValue.itemIdToViewList && this.mValue.itemIdToViewList.length > 0){
                let _emitViewItemMap = [];
                for(let i = 0;i<this.mValue.itemIdToViewList.length;i++){
                    if(this.mItem.viewId != this.mValue.itemIdToViewList[i]){
                        _emitViewItemMap[String(this.mValue.itemIdToViewList[i])] = 1;
                    }
                }
                let _emit = {};
                _emit.action = 'onMirrorEmitAction'
                _emit.data = {};
                _emit.data.itemId = this.mItem.itemId;
                _emit.data.mirrorViewItem = _emitViewItemMap;
                
                _emit.data.mirrorData = {};
                _emit.data.mirrorData.value = this.value;
                _emit.data.mirrorData.hiddenValue = this.hiddenValue;
                _emit.data.mirrorData.isRequired = this.isRequired;
                _emit.data.mirrorData.isVisible = this.isVisible;
                _emit.data.mirrorData.isReadonly = this.isReadonly;
                _emit.data.mirrorData.isEditable = this.isEditable;
                _emit.data.mirrorData.selItems = EcoUtil.objDeepCopy( this.selItems);

                this.$emit('emitEvent',_emit); 
            }
        },

        callMirror(data){
            if(data.mirrorData){
                this.value = data.mirrorData.value;
                this.hiddenValue = data.mirrorData.hiddenValue;
                this.isRequired = data.mirrorData.isRequired;
                this.isVisible = data.mirrorData.isVisible;
                this.isReadonly = data.mirrorData.isReadonly;
                this.isEditable = data.mirrorData.isEditable;
                this.selItems = data.mirrorData.selItems;
            }
        }
        /*镜像映射 结束*/
  },
  watch: {

  }
}
</script>
